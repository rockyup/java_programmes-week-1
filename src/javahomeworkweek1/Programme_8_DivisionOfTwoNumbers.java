package javahomeworkweek1;

/**
 * write a java program to divide two numbers and print on the screen
 * test data : 50/3
 * expected output: 16
 */
public class Programme_8_DivisionOfTwoNumbers {
    public static void main(String[] args) {
        // the two integers are value1 and value2
        int value1 = 50;
        int value2 = 3;
        int division = value1 / value2;
        // concatenation
        System.out.println("The " + value1 + " is divided by " + value2 + " the answer is " + division);
    }
}
